    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        String vehicleNo = vehiclenoField.getText().trim();
        String name = nameField.getText().trim();
        String version = versionField.getText().trim();
        String brand = brandField.getText().trim();
        String formattedDate;
        SimpleDateFormat fromUser = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat toDatabase = new SimpleDateFormat("yyyy-MM-dd");
        String service = serviceField.getSelectedItem().toString();
        
        String formatted = "";
        try{
            formattedDate = new SimpleDateFormat("yyyy-MM-dd").format(new SimpleDateFormat("dd-MM-yyyy").parse(dateField.getSelectedItem().toString()));
        } catch(ParseException e){
            JOptionPane.showMessageDialog(this, "Invalid date format");
            return;
        }
        if(vehicleNo.isEmpty()||name.isEmpty()||version.isEmpty()||brand.isEmpty()||formattedDate.isEmpty()||service.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill all fields before booking.");
            return;
        }
        
        try{
            Connection conn = db.DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement("INSERT INTO bookings(vehicle_no, name, version, brand, date, service) VALUES (?, ?, ?, ?, ?, ?)");
            ps.setString(1, vehicleNo);
            ps.setString(2, name);
            ps.setString(3, version);
            ps.setString(4, brand);
            ps.setString(5, formattedDate);
            ps.setString(6, service);
            
            int i = ps.executeUpdate();
            if(i > 0){
                JOptionPane.showMessageDialog(this, "Your Appointment has been successfully booked.");
                new BookingList().setVisible(true);
                this.dispose();
            }
            
            ps.close();
            conn.close();
        } catch (Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_bookBtnActionPerformed

