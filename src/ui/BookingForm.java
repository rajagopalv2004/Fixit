    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String vehicleNo = vehiclenoField.getText().trim();
        
        if(vehicleNo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter a Vehicle Number.");
            return;
        }
        
        try{
            Connection conn = db.DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM vehicles_details WHERE vehicle_no = ?");
            ps.setString(1, vehicleNo);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                
                // Autofill only these fields
                nameField.setText(rs.getString("name"));
                versionField.setText(rs.getString("version"));
                brandField.setText(rs.getString("brand"));   
                
            }else{
                JOptionPane.showMessageDialog(this, "No vehicle found with that number.");
            }
            
            rs.close();
            ps.close();
            conn.close();
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

